package com.ufc.explorequixada.Entity;

public class FriendEntity {
	private String id;
	private String name;
	private int qtdFriends;

	public FriendEntity() {

	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getQtdFriends() {
		return qtdFriends;
	}

	public void setQtdFriends(int qtdFriends) {
		this.qtdFriends = qtdFriends;
	}
}
